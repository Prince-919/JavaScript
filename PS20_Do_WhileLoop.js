
/********* do While Loop Statement *********/
// Difference while loop VS Do while loop

// while loop
console.log("While Loop Statement");
var a = 1;
while(a <= 10)
{
    console.log(a);
    a++;
}

console.log("Do While Loop Statement");

// do while loop

var i = 0;
do{
    console.log(i);
    i++;
}while(i <= 20);