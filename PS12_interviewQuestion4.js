// What is the Difference between == vs ===

var a = 5;
var b = '5';

// ==
console.log(" == ");
console.log(typeof(a));
console.log(typeof(b));
console.log(b);
console.log(a == b);

// ===
var x = 2;
var y = '2';
console.log(" === ");
console.log(typeof(x));
console.log(typeof(y));
console.log(y);
console.log(x === y);

